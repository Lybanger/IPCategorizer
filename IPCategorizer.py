import pandas as pd

rangos = [
    ("200.68.184.0", "200.68.191.255"),
    ("200.68.180.0", "200.68.183.255"),
    ("200.68.128.0", "200.68.131.255"),
    ("200.68.142.0", "200.68.143.255"),
    ("200.68.138.0", "200.68.139.255"),
    ("201.162.232.0", "201.162.235.255"),
    ("201.162.224.0", "201.162.227.255"),
    ("201.141.112.0", "201.141.127.255"),
    ("189.203.96.0", "189.203.111.255"),
    ("200.68.159.0", "200.68.159.255"),
    ("200.68.140.0", "200.68.141.255"),
    ("177.249.160.0", "177.249.175.255"),
    ("201.166.190.0", "201.166.191.255"),
    ("200.68.136.0", "200.68.137.255"),
]


def categorizar_ip(ip):
    for inicio, fin in rangos:
        if inicio <= ip <= fin:
            return f"{inicio} - {fin}"
    return 'Desconocido'


df = pd.read_csv("archivo.csv")
df['Rango'] = df['IP Address'].apply(categorizar_ip)

df.to_csv("archivo.csv", index=False)
